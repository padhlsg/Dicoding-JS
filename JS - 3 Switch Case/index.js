// Switch Case

let skorUjian = 80;
let hasilUjian = null;

switch (skorUjian) {
  case 90:
    hasilUjian = "Luar Biasa";
    break;
  case 80:
    hasilUjian = "Bagus";
    break;
  case 70:
    hasilUjian = "Kurang Bagus";
    break;

  default:
    hasilUjian = "Mana Nilainya Bang";
    break;
}

console.log(hasilUjian);
